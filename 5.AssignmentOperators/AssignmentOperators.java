

public class AssignmentOperators {
    public static void main(String args[]){
        int num1=8;
        int num2=5;
        num1 = num1 + 2;
        num1 +=4;
        num1++; // post - increment
        ++num1; // pre - increment
        int result = num1 + num2;

        System.out.println(result);
    }
    
}
